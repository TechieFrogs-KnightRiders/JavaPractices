public class Multidiarray {
    public static void main(String[] args) {
        int arr[][]={{1,2,3},{10,20,30},{20,50,3}};  //declaring and initializing 2D array
        for(int i=0;i<3;i++)
            {
                for(int j=0;j<3;j++)
                  {
                      System.out.println(arr[i][j]+" ");
                  }
                  System.out.println();
            }
    }
    
}
