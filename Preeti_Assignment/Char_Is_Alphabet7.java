public class Char_Is_Alphabet7 {
    public static void main(String[] args) {
        char ch = '6';
        if((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z'))
            System.out.println("It is an Alphabet");
        else
            System.out.println("It is not an Alphabet");
    }
    
}
